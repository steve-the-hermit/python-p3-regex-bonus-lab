import re

def fullmatch(pattern, string, flags=0):
    """
    Match the entire string against the pattern and return a match object if successful.
    """
    return re.fullmatch(pattern, string, flags)

def findall(pattern, string, flags=0):
    """
    Find all non-overlapping matches of the pattern in the string and return them as a list of strings.
    """
    return re.findall(pattern, string, flags)

